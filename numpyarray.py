import numpy as np

# Creating the array
arr = np.array([[1, 2, 3],
                [4, 5, 6]])

print("Array:")
print(arr)

# a) Number of dimensions
print("\nNumber of dimensions:", arr.ndim)

# b) Shape of the array
print("Shape of the array:", arr.shape)

# c) Size of the array
print("Size of the array:", arr.size)

# d) Type of elements
print("Type of elements:", arr.dtype)
